package com.example.bechitra.walleto.graph;

public class GraphData {
    private String xAxis, yAxis;

    public GraphData(String xAxis, String yAxis) {

        this.xAxis = xAxis;
        this.yAxis = yAxis;
    }

    public String getXAxis() {
        return this.xAxis;
    }

    public String getYAxis() {
        return this.yAxis;
    }
}
